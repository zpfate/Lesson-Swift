import Foundation
/**
 * 给你一个整数 x ，如果 x 是一个回文整数，返回 true ；否则，返回 false 。
 * 回文数是指正序（从左向右）和倒序（从右向左）读都是一样的整数。
 * 例如，121 是回文，而 123 不是。
 */

extension Solution {
    func isPalindrome(_ x: Int) -> Bool {
        guard x > 0 else { return false }
     
        // 1 2 3 4 5
        let string: String = "\(x)"
        for i in 0..<string.count {
            let last = string.count - 1 - i
            let suffix = string[last]
            if last == i || (last+1) == i {
                break
            }
            /// 如果两者不相等
            if string[i] != suffix {
                return false
            }
        }
        return true
    }
}
